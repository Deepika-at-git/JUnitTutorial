package com.tutorials;

public class MathUtils {

    public int add(int a, int b){
        return a+b;
    }

    public int multiply(int a, int b){
        return a*b;
    }

    public int divide(int a, int b){
        return a/b;
    }

    public double areaOfCircle(double radius){
        return Math.PI*radius*radius;
    }
}
